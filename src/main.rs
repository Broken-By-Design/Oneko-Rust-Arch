#[derive(Clone, Copy, PartialEq)]
enum CatState { Chasing, Sitting, Washing, Sleeping }

#[derive(Clone, Copy, PartialEq)]
enum Dir { N, NE, E, SE, S, SW, W, NW }

fn dir_from_delta(dx: f32, dy: f32) -> Dir {
    let adx = dx.abs();
    let ady = dy.abs();
    if adx > ady * 2.0 {
        if dx > 0.0 { Dir::E } else { Dir::W }
    } else if ady > adx * 2.0 {
        if dy > 0.0 { Dir::S } else { Dir::N }
    } else {
        match (dx >= 0.0, dy <= 0.0) {
            (true,  true)  => Dir::NE,
            (false, true)  => Dir::NW,
            (true,  false) => Dir::SE,
            (false, false) => Dir::SW,
        }
    }
}

fn mouse_pos() -> (f32, f32) {
    let Ok(output) = std::process::Command::new("hyprctl")
        .args(["cursorpos"])
        .output()
    else {
        return (0.0, 0.0);
    };
    let s = String::from_utf8_lossy(&output.stdout);
    let mut parts = s.trim().splitn(2, ", ");
    let x = parts.next().and_then(|v| v.parse().ok()).unwrap_or(0.0);
    let y = parts.next().and_then(|v| v.parse().ok()).unwrap_or(0.0);
    (x, y)
}

