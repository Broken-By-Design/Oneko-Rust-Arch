fn dir_from_delta(dx: f32, dy: f32) -> Dir {
    let adx = dx.abs();
    let ady = dy.abs();
    if adx > ady * 2.0 {
        if dx > 0.0 {
            Dir::E
        } else {
            Dir::W
        }
    } else if ady > adx * 2.0 {
        if dy > 0.0 {
            Dir::S
        } else {
            Dir::N
        }
    } else {
        match (dx >= 0.0, dy <= 0.0) {
            (true, true) => Dir::NE,
            (false, true) => Dir::NW,
            (true, false) => Dir::SE,
            (false, false) => Dir::SW,
        }
    }
}

